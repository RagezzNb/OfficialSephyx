// Product utility functions
window.PRODUCT_UTILS = {
  getProductById: (id) => {
    return window.PRODUCTS.find(product => product.id === id);
  },
  
  getProductsByCategory: (category) => {
    if (category === 'all') return window.PRODUCTS;
    return window.PRODUCTS.filter(product => product.category === category);
  },
  
  searchProducts: (query) => {
    const lowerQuery = query.toLowerCase();
    return window.PRODUCTS.filter(product => 
      product.name.toLowerCase().includes(lowerQuery) ||
      product.materials.toLowerCase().includes(lowerQuery) ||
      product.styling.toLowerCase().includes(lowerQuery)
    );
  },
  
  getRandomProducts: (count = 3) => {
    const shuffled = [...window.PRODUCTS].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
  },
  
  getFeaturedProducts: () => {
    // Return highest priced items as "featured"
    return [...window.PRODUCTS]
      .sort((a, b) => b.price - a.price)
      .slice(0, 6);
  },
  
  getRelatedProducts: (productId, count = 3) => {
    const product = window.PRODUCT_UTILS.getProductById(productId);
    if (!product) return [];
    
    // Get products from same category, excluding the current product
    const related = window.PRODUCTS
      .filter(p => p.category === product.category && p.id !== productId)
      .sort(() => 0.5 - Math.random())
      .slice(0, count);
    
    // If not enough from same category, fill with random products
    if (related.length < count) {
      const remaining = window.PRODUCTS
        .filter(p => p.id !== productId && !related.includes(p))
        .sort(() => 0.5 - Math.random())
        .slice(0, count - related.length);
      
      related.push(...remaining);
    }
    
    return related;
  }
};

